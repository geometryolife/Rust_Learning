fn main() {
    // 加法
    let sum = 5 + 10;
    println!("{}", sum);

    // 减法
    let difference = 95.5 - 4.3;
    println!("{}", difference);

    // 乘法
    let product = 4 * 30;
    println!("{}", product);

    // 除法
    let quotient = 56.7 / 32.2;
    println!("{}", quotient);

    // 取余
    let remainder = 43 % 5;
    println!("{}", remainder);
}
