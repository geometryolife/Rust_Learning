// 【10-19】这里的引用是有效的，因为数据的生命周期要比引用更长
fn main() {
    {
        let x = 5;            // ----------+-- 'b
                              //           |
        let r = &x;           // --+-- 'a  |
                              //   |       |
        println!("r: {}", r); //   |       |
                              // --+       |
    }                         // ----------+
}
// === Output ===
// r: 5
